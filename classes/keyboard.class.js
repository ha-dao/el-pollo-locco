/**
 * Manages keyboard input state
 */
class Keyboard {
    /**
     * Left arrow or 'A' key pressed state
     * @type {boolean}
     */
    LEFT = false;
    
    /**
     * Right arrow or 'D' key pressed state
     * @type {boolean}
     */
    RIGHT = false;
    
    /**
     * Up arrow or 'W' key pressed state
     * @type {boolean}
     */
    UP = false;
    
    /**
     * Down arrow or 'S' key pressed state
     * @type {boolean}
     */
    DOWN = false;
    
    /**
     * Space key pressed state
     * @type {boolean}
     */
    SPACE = false;
    
    /**
     * 'F' key pressed state
     * @type {boolean}
     */
    F = false;
}